	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
;R0 = 
	LDR R0, =ARR;	START POINTER
	LDR R1, =ARR;	ARR POINTER
	LDR R2, [R1];	ARR POINTER VALIUE
	MOV R3, #111;	MIN VALUE
	MOV R4, #6;		i
	MOV R5, #6;		j
	
	
LOOPI
	CMP R4, #0
	BEQ EXITI
	MOV R5, R4;		J=I
LOOPJ
	CMP R5, #0
	BEQ EXITJ
	LDR R2, [R1]
	CMP R3, R2
	MOVHI R3, R2
	MOVHI R8, R1
	SUB R4, #1
	SUB R5, #1
	ADD R1, #4
	B LOOPJ

EXITJ
	SUB R4, #1
	MOV R6, R3
	LDR R7, [R0]
	STR R7, [R3]
	STR R6, [R8]
	ADD R0, #4
	LDR R1, [R0]
	B LOOPI
	
EXITI


STOP
	B STOP
	

ARR DCD 0X4,0X1,0X3,0X5,0X8,0X2
	END
